%
%=============================================================================
% mdlOutputs
% Return the block outputs.
%=============================================================================
%
function sys=mdlOutputs(t,x,P)

    rx = P.alpha*cos(P.omega*t/2);
    ry = P.beta*sin(P.omega*t);
    rxdot = -P.omega*P.alpha/2*sin(P.omega*t/2);
    rydot = P.omega*P.beta*cos(P.omega*t);
    rxddot = -P.omega^2*P.alpha/4*cos(P.omega*t/2);
    ryddot = -P.omega^2*P.beta*sin(P.omega*t);
    
    psi = atan2(rydot,rxdot);
    V = sqrt(rxdot^2+rydot^2);
    u1 = (rxdot*ryddot - rydot*rxddot)/(rxdot^2+rydot^2);
    u2 = (rxdot*rxddot + rydot*ryddot)/V;
    
    sys = [u1; u2; rx; ry; psi; V];

% end mdlOutputs

